# in `/usr/share/themes/Ambiance/metacity-1/metacity-theme-1.xml`.
#
# The folowing parameters change the width of the borders:
#
#     <distance name="left_width" value="1"/>
#     <distance name="right_width" value="1"/>
#     <distance name="bottom_height" value="1"/>
#
# By default they are set to "1", but I found that changing them to "4" doesn't change the
# appearance very drastically, but makes it very much easier to resize using the mouse.
#
# To reload the theme without logging out you can run the command:
#
#     metacity-message reload-theme


